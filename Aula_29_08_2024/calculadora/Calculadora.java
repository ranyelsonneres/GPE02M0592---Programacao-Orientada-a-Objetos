package calculadora;

public class Calculadora {
	
	//atributos
	double num1;
	double num2;
	
	//m√©todos
	//somar
	public double somar() {
		return num1 + num2;
	}
	//subtrair
	public double subtrair() {
		return num1 - num2;
	}
	//dividir
	public double dividir() {
		return num1 / num2;
	}
	//multiplicar
	public double mult() {
		return num1 * num2;
	}

}
